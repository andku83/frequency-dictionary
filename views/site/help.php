<h1><?= $this->title ?></h1>

<h3>Welcome to <strong>A Semantic Frequency Dictionary of Information Technologies</strong>.</h3>
<p>This easy-to-use program will benefit those who are trying to learn vocabulary in Information Technologies.</p>
<p>The Dictionary is based on the actual frequency of words in a Text Corpus of Wikipedia articles (Category:</p>
<p>Information Technologies).</p>
<p>In addition, the user can develop his own Semantic Frequency Dictionary using a new Text Corpus.</p>
<p>The Dictionary contains the most frequent words, the context of their usage, main meanings, and scores of</p>
<p>frequency and dispersion.</p>
<br>
<h3>How to use <strong>A Semantic Frequency Dictionary of Information Technologies</strong>:</h3>
<p><strong>1 Step</strong> (Text Corpus)</p>
<p>Load texts which the Dictionary will be based on.</p>
<br>
<p><strong>2 Step</strong> (Processing)</p>
<p>The program is processing the corpus to extract all the frequent words.</p>
<br>
<p><strong>3 Step</strong> (Frequency Dictionary)</p>
<p>The result of the text corpus processing is shown in the table (Dictionary).</p>
<p><strong>File</strong> shows the list of loaded texts</p>
<p><strong>Score</strong> presents the score of Frequency * Dispersion</p>
<p><strong>Headword</strong> contains a rank-ordered listing of all the frequent words, starting with the most frequent word.</p>
<p>Clicking on a word, its definition is shown. In case of an absent definition, the user can add it</p>
<p><strong>Frequency</strong> presents the score of Q x (number of x-word occurrences) / Q all (total number of words occurrences)</p>
<p><strong>Dispersion</strong> presents the score of R x (number of texts with x-word) / R all (total number of texts)</p>
<p><strong>Context</strong> contains names of texts and sentences with headwords (context of their usage)</p>
<br>
<p><strong>Supporting Information</strong> (Glossary)</p>
<p>The program provides its dynamic glossary of definitions for computer-related terms</p>
<br><br>

<p><i>We hope that the program will benefit those who are trying to learn Information Technologies terms, <br>as well as for those who desire to assist them.</i></p>